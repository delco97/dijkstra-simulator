    private void jLabel_confLinkMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_confLinkMouseClicked
        // TODO add your handling code here:
        String str_start = jTextField_startLink.getText();
        String str_end = jTextField_endLink.getText();
        String str_peso = jTextField_pesoLink.getText();
        int start,end;
        double peso;
        try{
          start =Integer.parseInt(str_start); 
          end = Integer.parseInt(str_end);
          peso = Double.parseDouble(str_peso);
          if(!g.esisteArco(start,end,peso)){//Evita la creazione di archi identici
             if(g.addArco(Integer.parseInt(str_start),Integer.parseInt(str_end),Double.parseDouble(str_peso)))
                jTextArea_grafoStruct.setText(g.toString());
             else //
                JOptionPane.showMessageDialog(this, "Uno o entrambi i nodi inseriti non si trovano nel grafo","Attenzione", JOptionPane.WARNING_MESSAGE);  
          }
          else
            JOptionPane.showMessageDialog(this, "Non si possono inserire archi identici.","Attenzione", JOptionPane.WARNING_MESSAGE);
        }catch(Exception e){
          JOptionPane.showMessageDialog(this, "Valori inseriti non validi","Attenzione", JOptionPane.WARNING_MESSAGE);        
        }
          
    }//GEN-LAST:event_jLabel_confLinkMouseClicked

