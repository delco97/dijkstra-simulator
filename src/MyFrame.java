    private void jLabel_confDijkstraMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jLabel_confDijkstraMouseClicked
        // TODO add your handling code here:
        String str_start = jTextField_startDijkstra.getText();
        String str_end = jTextField_endDijkstra.getText();
        int start=-1,end=-1;

        try{
          CamminiMinimi c = new CamminiMinimi(g);//Può fallire se il grafo ancora non esiste
          start = Integer.parseInt(str_start);//Può fallire se la string non è intera
          if(!str_end.equals(""))//Tenta la conversione solo se il campo NON è vuoto
            end = Integer.parseInt(str_end);//Può fallire se la string non è intera
          if(g.nodoValido(start)){
            c.exeDijkstra(start);  
            jTextArea_result.setText(c.toString());//Stampo tabella di Dijkstra
            if(g.nodoValido(end)){//Se viene inserito il nodo di arrivo
              jTextArea_result.setText(jTextArea_result.getText()+"\nCammino("+start+"->"+end+") : "+c.cammino(start,end));//Può fallire se str_end non è un intero
              System.out.println("cammino");
            }
          }
          else{
            JOptionPane.showMessageDialog(this, "Nodo di partenza non valido","Attenzione", JOptionPane.WARNING_MESSAGE);        
          }
              //jTextArea_result.setText(c.toString());//Stampo tabella di Dijkstra

        }catch(Exception e){
          JOptionPane.showMessageDialog(this, "Non è possibile eseguire Dijkstra","Attenzione", JOptionPane.WARNING_MESSAGE);        
        }        
        
    }//GEN-LAST:event_jLabel_confDijkstraMouseClicked

